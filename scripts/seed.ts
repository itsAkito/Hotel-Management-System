async function seed() {
  try {
    console.log("üå± Starting database seed...");

    for (const hotelData of DEMO_HOTELS) {
      const { rooms, ...hotelInfo } = hotelData;

      // Create hotel
      const hotel = await prismadb.hotel.create({
        data: {
          ...hotelInfo,
          rooms: {
            create: rooms,
          },
        },
        include: {
          rooms: true,
        },
      });

      console.log(`‚úÖ Created hotel: ${hotel.title}`);
    }

    console.log("üéâ Seed completed successfully!");
  } catch (error) {
    console.error("‚ùå Error seeding database:", error);
    process.exit(1);
  }
}

seed();
